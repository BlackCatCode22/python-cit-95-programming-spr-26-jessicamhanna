# sum_of_two_ints.py
# Inputs two integers from user, computes SUM, outputs result

# Input: Get two integers from user
num1 = int(input("Enter first integer: "))
num2 = int(input("Enter second integer: "))

# Process: Compute the SUM
total = num1 + num2

# Output: Display the sum
print("Sum:", total)


